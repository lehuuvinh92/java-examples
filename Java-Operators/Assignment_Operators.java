package com.gochocit.examples;

/**
 *
 * @author LHVINH
 */
public class Assignment_Operators {

    public static void main(String[] args) {
        // create variables
        int a = 4;
        int var;

        // assign value using =
        var = a;
        System.out.println("var using =: " + var);

        // assign value using =+
        var += a;
        System.out.println("var using +=: " + var);

        // assign value using =*
        var *= a;
        System.out.println("var using *=: " + var);
    }
}
